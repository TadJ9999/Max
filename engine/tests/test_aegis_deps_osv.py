PACKAGE_LOCK_V3 = {
    "name": "app",
    "lockfileVersion": 3,
    "packages": {
        "node_modules/react": {"version": "18.2.0"},
        "node_modules/typescript": {"version": "5.4.5"},
        "node_modules/dev-tool": {"version": "1.0.0", "dev": True},
        "": {},  # root package entry — should be skipped
    },
}


def test_parse_npm_v3(tmp_path: Path):
    p = write(tmp_path, "package-lock.json", json.dumps(PACKAGE_LOCK_V3))
    pkgs = _parse_npm(p)
    names = {pkg.name for pkg in pkgs}
    assert "react" in names
    assert "typescript" in names
    # dev deps should be excluded
    assert "dev-tool" not in names
    assert all(pkg.ecosystem == "npm" for pkg in pkgs)
    assert all(pkg.version for pkg in pkgs)


def test_parse_npm_missing_file(tmp_path: Path):
    pkgs = _parse_npm(tmp_path / "nonexistent.json")
    assert pkgs == []


def test_parse_npm_invalid_json(tmp_path: Path):
    p = write(tmp_path, "package-lock.json", "not json {{{")
    pkgs = _parse_npm(p)
    assert pkgs == []


