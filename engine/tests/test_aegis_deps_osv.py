def test_query_osv_prefers_cve_id():
    """CVE alias should be preferred over GHSA in cve_id field."""
    packages = [Package("PyPI", "requests", "2.28.0")]
    mock_resp = MagicMock()
    mock_resp.status_code = 200
    mock_resp.json.return_value = {
        "results": [{"vulns": [{
            "id": "GHSA-xxxx-yyyy",
            "aliases": ["CVE-2024-9999"],
            "summary": "Test",
            "database_specific": {"severity": "CRITICAL"},
            "affected": [],
        }]}]
    }
    mock_client = AsyncMock()
    mock_client.post = AsyncMock(return_value=mock_resp)
    mock_client.aclose = AsyncMock()

    findings = asyncio.run(query_osv(packages, client=mock_client))
    assert findings[0]["cve_id"] == "CVE-2024-9999"
    assert findings[0]["severity"] == "Critical"


