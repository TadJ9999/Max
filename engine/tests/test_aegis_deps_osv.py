# Python manifest parser
# ---------------------------------------------------------------------------

PYPROJECT = """\
    [project]
    name = "max-engine"
    version = "0.1.0"
    dependencies = [
        "fastapi>=0.111.0",
        "httpx>=0.27.0",
        "pydantic>=2.7.0",
        "uvicorn>=0.29.0",
    ]

    [project.optional-dependencies]
    dev = [
        "pytest>=8.0.0",
        "ruff>=0.4.0",
    ]
"""


def test_parse_python_direct_deps(tmp_path: Path):
    p = write(tmp_path, "pyproject.toml", PYPROJECT)
    pkgs = _parse_python(p)
    names = {pkg.name for pkg in pkgs}
    assert "fastapi" in names
    assert "httpx" in names
    assert all(pkg.ecosystem == "PyPI" for pkg in pkgs)
    assert all(pkg.version for pkg in pkgs)


def test_parse_python_optional_deps(tmp_path: Path):
    p = write(tmp_path, "pyproject.toml", PYPROJECT)
    pkgs = _parse_python(p)
    names = {pkg.name for pkg in pkgs}
    assert "pytest" in names


def test_parse_python_missing_file(tmp_path: Path):
    pkgs = _parse_python(tmp_path / "nonexistent.toml")
    assert pkgs == []


def test_parse_python_normalises_name(tmp_path: Path):
    p = write(tmp_path, "pyproject.toml", """\
        [project]
        dependencies = ["My_Package>=1.0.0"]
    """)
    pkgs = _parse_python(p)
    assert pkgs[0].name == "my-package"


