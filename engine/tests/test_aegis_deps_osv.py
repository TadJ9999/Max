OSV_BATCH_RESPONSE = {
    "results": [
        {
            "vulns": [{
                "id": "GHSA-test-1234",
                "aliases": ["CVE-2024-1234"],
                "summary": "Test vulnerability in requests",
                "details": "A test vulnerability.",
                "database_specific": {"severity": "HIGH"},
                "affected": [{
                    "package": {"name": "requests", "ecosystem": "PyPI"},
                    "ranges": [{"type": "ECOSYSTEM", "events": [
                        {"introduced": "0"}, {"fixed": "2.32.0"}
                    ]}]
                }]
            }]
        },
        {"vulns": []},  # no vulns for second package
    ]
}


def test_query_osv_returns_findings():
    packages = [
        Package("PyPI", "requests", "2.28.0"),
        Package("PyPI", "httpx", "0.27.0"),
    ]
    mock_resp = MagicMock()
    mock_resp.status_code = 200
    mock_resp.json.return_value = OSV_BATCH_RESPONSE

    mock_client = AsyncMock()
    mock_client.post = AsyncMock(return_value=mock_resp)
    mock_client.aclose = AsyncMock()

    findings = asyncio.run(query_osv(packages, client=mock_client))
    assert len(findings) == 1
    f = findings[0]
    assert f["cve_id"] == "CVE-2024-1234"
    assert f["package"] == "requests"
    assert f["installed_version"] == "2.28.0"
    assert f["fixed_version"] == "2.32.0"
    assert f["severity"] == "High"
    assert f["category"] == "sca"


def test_query_osv_empty_packages():
    findings = asyncio.run(query_osv([]))
    assert findings == []


